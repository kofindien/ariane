<strong><u>Avantages</u></strong> :<br /><br />
<u>Pour les particuliers :</u><br /><br />
<ul>
  <li> Utilisable immédiatement : dès le rechargement dans une agence UBA ou dans les points agréés ;</li>
  <li> Zéro frais de gestion mensuelle ;</li>
  <li> Disponibilité immédiate des fonds, en cas de réception d'un transfert sur carte, évitant ainsi les files d'attentes dans les banques ;</li>
  <li> Sécurité : la carte évite les risques liés au transport d'espèces (par exemple lors des voyages).</li>
</ul>
  <u>Pour les entreprises / organisations</u> :<br /><br />
  <ul>
  <li>Simplification de la gestion des paiements à des tiers (paies, frais de missions, primes, bourses, etc.) ;</li>
  <li>Personnalisable selon vos besoins, la carte peut etre faconnée pour servir de carte d'abonné, de carte d'étudiant, etc.</li>
</ul>